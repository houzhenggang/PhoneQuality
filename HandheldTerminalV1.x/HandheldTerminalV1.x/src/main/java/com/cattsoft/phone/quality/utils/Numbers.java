package com.cattsoft.phone.quality.utils;

import java.util.List;

/**
 * Created by Xiaohong on 2014/4/4.
 */
public class Numbers {
    /**
     * 传入的值大于等于0时将返回原值，小于0时返回0
     *
     * @param v
     * @return
     */
    public static long getNegativeZero(long v) {
        return v > 0 ? v : 0;
    }

    public static double sum(double[] value) {
        double sum = 0;
        for (double number : value)
            sum += number;
        return sum;
    }

    public static double sum(List<Double> value) {
        double sum = 0;
        for (double number : value)
            sum += number;
        return sum;
    }

    public static long sumLong(List<Long> value) {
        long sum = 0;
        for (long number : value)
            sum += number;
        return sum;
    }
}
